                       //with WHILE LOOP
let i = 1;
while (i <= 5){
    if(i % 2 === 0){
        console.log(i,'Odd Number');
    }else {
        console.log(i,'Even Number');
    }
    i++
}

                        //with DO/WHILE LOOP

let j = 1;
do{
    if(j % 2 === 0){
        console.log(j, 'Odd Number');
    }else{
        console.log(j, 'Even Number');
    }
    j++
}while(j <= 5);


